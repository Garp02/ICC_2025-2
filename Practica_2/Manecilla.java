public class Manecilla
{
    private int valor;
    
    // Constructor
    public Manecilla(int valor)
    {
        this.valor = 0;
    }

    // Método de acceso
    public int getValor()
    {
        return this.valor;
    }

    // Método mutante
    public void setValor(int valor)
    {
        this.valor = valor;
    }
}